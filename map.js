// map ---- transform contents of array

// map does not effect original array

// always returns a new array

let a = [ 1, 2, 3, 4, 5];

// [1, 4, 9, 16, 25]

// print square of each element of array

const squaredArray = a.map(function (num) {
    return num * num;
})

console.log("squared Array", squaredArray);

