// ++ Increment operator

let x = 20;

console.log(x++); // post increment
// x++ = x= x+1

// ++x = x = x + 1
console.log(++x); // pre increment

// -- Decrement operator

let y = 5;

console.log(y--); // 5
y = y - 1;

console.log(y--); // 4
y = y - 1; // 3

y = y - 1; // 2
console.log(--y); // 2