// && || !

let x= 20;
let y = 45;

console.log((x>y) && (y == 45)); // false
console.log((x>y) || (y == 45)); // true
console.log(!(y == 45)); // false

/**
 * && --- if both the conditions are true , then only my output will be true
 * || --- if any one or both conditions are true , then my output will be true
 */

